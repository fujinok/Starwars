namespace Starwars.Models
{
    /// <summary>
    /// Ship Model.
    /// </summary>
    public class Ship
    {
        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        /// <value>
        /// The name.
        /// </value>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the MGLT.
        /// </summary>
        /// <value>
        /// The MGLT.
        /// </value>
        public string MGLT { get; set; }

        /// <summary>
        /// Gets or sets the consumables.
        /// </summary>
        /// <value>
        /// The consumables.
        /// </value>
        public string Consumables { get; set; }
    }
}
